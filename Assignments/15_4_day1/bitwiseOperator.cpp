#include <iostream>

using namespace std;

int main()
{
    int a = 5;

    cout << a & 2 << endl;
    cout << a | 4 << endl;
    cout << ~a << endl;
    cout << a ^ 2 << endl;
    cout << a >> 2 << endl;
    cout << a << 3 << endl;
}